LLMCallable = Callable[[List[Dict[str, str]]], Awaitable[str]]
"""Async function ``(messages) -> assistant text``.

Tests inject a mock implementation; production wires to ``llm.client``.
"""


# ---------------------------------------------------------------------------
# Default deterministic LLM (test fallback)
# ---------------------------------------------------------------------------


async def _deterministic_llm(messages: List[Dict[str, str]]) -> str:
    """Deterministic offline LLM stand-in used when no LLM is configured.

    Generates plausible section content from the system prompt's section
    title, so tests can run without network or real model access.
    """
    sys_msg = next((m["content"] for m in messages if m["role"] == "system"), "")
    user_msg = next((m["content"] for m in messages if m["role"] == "user"), "")

    # Extract a section hint from system prompt
    title_match = re.search(r'section[^:]*:\s*"?([^"\n]+)"?', sys_msg, re.IGNORECASE)
    section = title_match.group(1).strip() if title_match else "Analysis"

    return (
        f"Based on the provided context, the **{section}** assessment finds "
        "that key signals are present and warrant attention. Three concrete "
        "observations support this view: (1) recent activity is consistent "
        "with the stated requirement; (2) the data points align with prior "
        "patterns; (3) downstream actions remain feasible.\n\n"
        f"_Note: deterministic stand-in answer for prompt of length "
        f"{len(user_msg)} chars._"
    )


