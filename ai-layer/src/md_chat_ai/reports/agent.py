"""
ReportAgent for MD-Chat AI layer.

Adapts the Cronberry ReportAgent to MD-Chat with these differences from the
original implementation:

  1. **Async API** — ``async def generate(...)`` is the canonical entry point.
     The original sync ``generate_report`` is preserved for compatibility.
  2. **Confidential compute marker** — every report carries
     ``metadata.compute_backend`` in {"on_device", "router_pcc", "router_open"}
     describing where the LLM call physically executed. Critical for AI Act
     compliance + EU sovereignty story.
  3. **AI Act Article 50 disclosure** — every rendered Markdown report ends
     with the localized disclosure footer ("You are interacting with an AI
     system...") sourced from ``Config.ai_disclosure_*``.
  4. **Multi-language** — templates resolved per ``language`` (ro/ru/en).
  5. **PII redaction** — ``apply_pii_redaction`` integrates with the
     ``security.prompt_guard.PIIRedactor`` once it lands; today we ship a
     defensive shim that no-ops gracefully when the module is absent.

The graph-aware tool implementations from Cronberry (Zep, SQLite, Supabase)
are intentionally *not* ported — md-chat reads from Neo4j + Synapse via
``context`` injected by the caller. The agent therefore provides a clean,
context-driven generation path without the legacy ReACT loop dependency.
A simplified tool-less generation strategy is used for tests; the ReACT
hooks remain available for future Neo4j tool wiring.
"""

