class ReportAgent:
    """
    Generate structured reports with confidential-compute awareness.

    Example::

        agent = ReportAgent()
        report = await agent.generate(
            "daily_digest",
            context={"messages_today": 42, "active_chats": 7},
            language="ro",
        )
        print(report.to_markdown())
    """

    def __init__(
        self,
        llm: Optional[LLMCallable] = None,
        compute_backend: ComputeBackend = "router_pcc",
        redact_pii: bool = True,
    ) -> None:
        if compute_backend not in VALID_COMPUTE_BACKENDS:
            raise ValueError(
                f"Invalid compute_backend '{compute_backend}'. "
                f"Must be one of {VALID_COMPUTE_BACKENDS}."
            )
        self._llm: LLMCallable = llm or _deterministic_llm
        self.compute_backend: ComputeBackend = compute_backend
        self.redact_pii = redact_pii

    # ------------------------------------------------------------------
    # Public async API
    # ------------------------------------------------------------------

    async def generate(
        self,
        template_name: str,
        context: Optional[Dict[str, Any]] = None,
        *,
        language: Language = "ro",
        requirement: Optional[str] = None,
        compute_backend: Optional[ComputeBackend] = None,
    ) -> Report:
        """Generate a report for ``template_name`` in ``language``.

        Args:
            template_name: Logical template key (e.g. ``"daily_digest"``).
            context: Arbitrary structured payload passed to the section prompts
                (e.g. message counts, channel stats, transcript text).
            language: One of ``"ro"`` / ``"ru"`` / ``"en"``.
            requirement: Free-text requirement / user prompt. Defaults to the
                template description.
            compute_backend: Override the agent's default compute backend for
                this single report.
        """
        if language not in SUPPORTED_LANGUAGES:
            raise ValueError(f"Unsupported language '{language}'. Supported: {SUPPORTED_LANGUAGES}")

        backend: ComputeBackend = compute_backend or self.compute_backend
        if backend not in VALID_COMPUTE_BACKENDS:
            raise ValueError(f"Invalid compute_backend '{backend}'")

        template = get_template(template_name, language=language)
        if template is None:
            raise KeyError(f"Unknown template '{template_name}'")

        ctx = context or {}
        req = requirement or template.description

        report_id = f"rpt_{uuid.uuid4().hex[:12]}"
        started = datetime.utcnow()

        report = Report(
            report_id=report_id,
            title=template.name,
            requirement=req,
            template_key=template.key,
            language=language,
            status=ReportStatus.GENERATING,
            compute_backend=backend,
            metadata={
                "template_name": template.name,
                "context_keys": sorted(ctx.keys()) if isinstance(ctx, dict) else [],
                "ai_act_disclosure": True,
            },
        )

        try:
            sections: List[ReportSection] = []
            for ts in template.sections:
                sec = await self._generate_section(
                    template=template,
                    section_def=ts,
                    requirement=req,
                    context=ctx,
                    previous=sections,
                )
                sections.append(sec)
            report.sections = sections
            report.status = ReportStatus.COMPLETED
        except Exception as exc:
            report.status = ReportStatus.FAILED
            report.error = str(exc)
            logger.exception("Report generation failed for %s", template_name)

        # PII redaction over assembled section content
        if self.redact_pii and report.sections:
            any_redacted = False
            for sec in report.sections:
                redacted, did = apply_pii_redaction(sec.content)
                if did:
                    sec.content = redacted
                    any_redacted = True
            report.pii_redacted = any_redacted

        finished = datetime.utcnow()
        report.completed_at = finished.isoformat() + "Z"
        report.generation_time_seconds = round((finished - started).total_seconds(), 3)
        return report

    # Sync wrapper for legacy callers
    def generate_report(
        self,
        template_name: str,
        context: Optional[Dict[str, Any]] = None,
        *,
        language: Language = "ro",
        requirement: Optional[str] = None,
        compute_backend: Optional[ComputeBackend] = None,
    ) -> Report:
        """Synchronous wrapper around :meth:`generate`."""
        return asyncio.run(
            self.generate(
                template_name,
                context=context,
                language=language,
                requirement=requirement,
                compute_backend=compute_backend,
            )
        )

    # ------------------------------------------------------------------
    # Section generation
    # ------------------------------------------------------------------

    async def _generate_section(
        self,
        template: ReportTemplate,
        section_def: TemplateSection,
        requirement: str,
        context: Dict[str, Any],
        previous: List[ReportSection],
    ) -> ReportSection:
        """Run one LLM call per section."""
        prev_titles = ", ".join(s.title for s in previous) or "(none)"

        # Sanitize free-text context fields before sending to LLM
        safe_context: Dict[str, Any] = {}
        for key, val in context.items():
            if isinstance(val, str) and self.redact_pii:
                safe_val, _ = apply_pii_redaction(val)
                safe_context[key] = safe_val
            else:
                safe_context[key] = val

        try:
            context_json = json.dumps(safe_context, ensure_ascii=False, default=str, indent=2)
        except (TypeError, ValueError):
            context_json = repr(safe_context)

        system_prompt = (
            f"You are writing the section: \"{section_def.title}\" of report "
            f"\"{template.name}\" in language={template.language}.\n"
            f"Description: {section_def.description}\n"
            f"Guidelines: {section_def.guidelines}\n"
            f"Target length: {section_def.min_words}-{section_def.max_words} words.\n"
            f"Previously written sections: {prev_titles}.\n"
            "Output Markdown only. Do not add a section header (it is added "
            "automatically). Do not invent facts that are absent from the "
            "context. Write in the report's language."
        )

        user_prompt = (
            f"Requirement: {requirement}\n\n"
            f"Context (JSON):\n{context_json}\n\n"
            "Write the section now."
        )

        messages = [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ]

        response = await self._llm(messages)
        viz_data, clean = _extract_visualization(response)

        return ReportSection(
            title=section_def.title,
            content=clean.strip(),
            evidence=[f"context_keys={sorted(safe_context.keys())}"],
            tool_calls_made=0,
            visualization_data=viz_data,
        )


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _extract_visualization(content: str) -> Tuple[Optional[Dict[str, Any]], str]:
    """Pull a ```json:visualization``` fenced block out of LLM text."""
    pattern = re.compile(r"```json:visualization\s*(.*?)```", re.DOTALL)
    match = pattern.search(content)
    if not match:
        return None, content
    try:
        viz = json.loads(match.group(1).strip())
    except json.JSONDecodeError:
        return None, content
    return viz, pattern.sub("", content).strip()


